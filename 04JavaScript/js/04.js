// String o Cadenas de Texto

const producto = 'Aprendiendo JavaScript'
const producto2 = 'Monitor HD'

// length es para la extension
console.log(producto.length);
console.log(producto2);

// IndexOf (retorna posicion)
console.log(producto.indexOf('JavaScript'));
console.log(producto2.indexOf('Tablet'));

// Includes (retorna true o false)
console.log(producto.includes('JavaScript'));
console.log(producto2.includes('Tablet'));