// Orden de las operaciones

let resultado;

resultado = (20 + 30) * 2; // es igual que en la escuela. Con parentesis le damos prioridad a una operacion.

resultado = (100 + 200 + 300) * .2; // Asi podemos sacar el porcentaje de una compra en una tienda en linea por ejemplo

console.log(resultado);

// Incrementos

let puntaje = 10;

// Incrementos en 1 ++
puntaje++; // 10 +1 primero el valor y luego el incremento
++puntaje; // +1 12 Primero el incremento y luego el valor

puntaje--; // hace lo mismo pero en decrecion ahora es -1



console.log(puntaje);

